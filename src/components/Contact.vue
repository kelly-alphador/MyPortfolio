<script>
export default {
  name: 'ContactSection',
  data() {
    return {
      form: {
        name: '',
        email: '',
        subject: '',
        message: ''
      },
      errors: {},
      isSubmitting: false,
      submitStatus: null
    }
  },
  mounted() {
    this.setupIntersectionObserver();
  },
  methods: {
    setupIntersectionObserver() {
      const observerAOS = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
          if (entry.isIntersecting) {
            entry.target.classList.add('contact-card-visible');
          }
        });
      }, { threshold: 0.1 });

      const contactCards = document.querySelectorAll('.contact-card-animated');
      contactCards.forEach((item, index) => {
        item.style.transitionDelay = `${index * 0.1}s`;
        observerAOS.observe(item);
      });
    },

    validateForm() {
      this.errors = {};
      
      if (!this.form.name.trim()) {
        this.errors.name = 'Le nom est requis';
      } else if (this.form.name.trim().length < 2) {
        this.errors.name = 'Le nom doit contenir au moins 2 caractères';
      }

      if (!this.form.email.trim()) {
        this.errors.email = 'L\'email est requis';
      } else if (!/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(this.form.email)) {
        this.errors.email = 'Format d\'email invalide';
      }

      if (!this.form.subject.trim()) {
        this.errors.subject = 'Le sujet est requis';
      }

      if (!this.form.message.trim()) {
        this.errors.message = 'Le message est requis';
      } else if (this.form.message.trim().length < 10) {
        this.errors.message = 'Le message doit contenir au moins 10 caractères';
      }

      return Object.keys(this.errors).length === 0;
    },

    async submitForm() {
      if (!this.validateForm()) {
        return;
      }

      this.isSubmitting = true;
      this.submitStatus = null;

      try {
        await new Promise(resolve => setTimeout(resolve, 2000));
        
        this.submitStatus = {
          type: 'success',
          message: 'Message envoyé avec succès ! Je vous répondrai dans les plus brefs délais.'
        };

        this.form = {
          name: '',
          email: '',
          subject: '',
          message: ''
        };
      } catch (error) {
        this.submitStatus = {
          type: 'error',
          message: 'Erreur lors de l\'envoi du message. Veuillez réessayer.'
        };
      } finally {
        this.isSubmitting = false;
        
        setTimeout(() => {
          this.submitStatus = null;
        }, 5000);
      }
    }
  }
}
</script>

